public class IntegerArithmetic {
    public static void main(String[] args) {
        int m = 25;
        int n = 7;
        System.out.println("m = " + m);
        System.out.println("n = " + n);
        int suma = m + n;
        System.out.println("m + n = " + suma);
        int diferencia = m - n;
        System.out.println("m - n = " + diferencia);
        int producto = m * n;
        System.out.println("m * n = " + producto);
        int cociente = m / n;
        System.out.println("m / n = " + cociente);
        int resto = m % n;
        System.out.println("m % n = " + resto);
    }
}
    